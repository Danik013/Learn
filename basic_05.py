def main():
    for n in range(10):
        random_skills = random.sample(SKILLS, 3)
        runic_skills = []
        for word in random_skills:
            runic_skill = word
            for old_letter, new_letter in LETTERS_MAPPING.items():
                runic_skill = runic_skill.replace(old_letter, new_letter)
            runic_skills.append(runic_skill)

        context = {
            "first_name": FAKER.first_name_male(),
            "last_name": FAKER.last_name_male(),
            "job": FAKER.job(),
            "town": FAKER.city(),
            "strength": random.randint(3, 18),
            "agility": random.randint(3, 18),
            "endurance": random.randint(3, 18),
            "intelligence": random.randint(3, 18),
            "luck": random.randint(3, 18),
            "skill_1": runic_skills[0],
            "skill_2": runic_skills[1],
            "skill_3": runic_skills[2]
        }

        file_operations.render_template("charsheet.svg", "output/svg/charsheet-{n}.svg".format(n=n), context)

if __name__ == "__main__":
	main()
